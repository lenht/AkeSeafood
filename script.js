function applyTranslations() {
  document.documentElement.lang = currentLang === "zh" ? "zh-CN" : currentLang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  });
  document.querySelectorAll(".lang-button").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === currentLang);
  });
  renderCategories();
  renderCatalog();
  updateSelectedSummary();
}

function renderCategories() {
  categoryTabs.innerHTML = categoryKeys
    .map(
      (key) =>
        `<button type="button" class="${key === activeCategory ? "is-active" : ""}" data-category="${key}">${t(key)}</button>`
    )
    .join("");
}

function renderProductCard(product) {
  const id = products.indexOf(product);
  const isSelected = selectedItems.has(id);
  return `
    <article class="product-card ${isSelected ? "is-selected" : ""}" data-product="${id}" tabindex="0" role="button" aria-pressed="${isSelected}">
      <div class="product-image-wrap">
        <img class="product-image" src="${productImage(product, id)}" alt="${escapeHtml(itemName(product))}" loading="lazy" />
      </div>
      <div class="product-copy">
        <h3>${escapeHtml(itemName(product))}</h3>
        <p>${secondaryNames(product).map(escapeHtml).join(" · ")}</p>
      </div>
      <div class="product-meta">
        <span class="pill">${t(product.cat)}</span>
        <button class="select-item ${isSelected ? "is-selected" : ""}" type="button" data-product="${id}">
          ${isSelected ? t("selected") : t("select")}
        </button>
      </div>
    </article>
  `;
}

function panelPreviewCount() {
  if (window.matchMedia("(max-width: 560px)").matches) return 1;
  if (window.matchMedia("(max-width: 880px)").matches) return 2;
  if (window.matchMedia("(max-width: 1120px)").matches) return 3;
  return 4;
}

function renderCategoryPanel(category, items) {
  const isExpanded = expandedCategories.has(category);
  const previewCount = panelPreviewCount();
  const visibleItems = isExpanded ? items : items.slice(0, previewCount);
  const hasToggle = items.length > previewCount;
  return `
    <section class="category-panel" data-category-panel="${category}">
      <div class="category-panel-header">
        <div>
          <h3>${t(category)}</h3>
          <span>${items.length} ${t("itemCount")}</span>
        </div>
        ${
          hasToggle
            ? `<button class="panel-toggle" type="button" data-panel-toggle="${category}" aria-expanded="${isExpanded}">
                ${isExpanded ? t("less") : t("more")}
              </button>`
            : ""
        }
      </div>
      <div class="category-panel-grid">
        ${visibleItems.map(renderProductCard).join("")}
      </div>
    </section>
  `;
}

function renderCatalog() {
  const query = normalizeSearchText(searchInput.value.trim());
  const filtered = products.filter((product) => {
    const matchesCategory = activeCategory === "all" || product.cat === activeCategory;
    const haystack = normalizeSearchText(`${product.en} ${product.vi} ${product.zh}`);
    return matchesCategory && haystack.includes(query);
  });

  if (!filtered.length) {
    catalogGrid.innerHTML = `<p class="empty-card">${t("noResults")}</p>`;
    return;
  }

  const visibleCategories = categoryKeys
    .filter((key) => key !== "all")
    .filter((key) => activeCategory === "all" || activeCategory === key);

  catalogGrid.innerHTML = visibleCategories
    .map((category) => {
      const items = filtered.filter((product) => product.cat === category);
      return items.length ? renderCategoryPanel(category, items) : "";
    })
    .join("");
}

function updateSelectedSummary() {
  if (!selectedItems.size) {
    selectedSummary.textContent = t("selectedEmpty");
    return;
  }

  selectedSummary.textContent = Array.from(selectedItems)
    .map((id) => itemName(products[id]))
    .join("\n");
}

