function t(key) {
  return translations[currentLang][key] || translations.en[key] || key;
}

function itemName(product) {
  return product[currentLang] || product.en;
}

function secondaryNames(product) {
  return [product.zh, product.vi, product.en].filter((name, index, arr) => name && name !== itemName(product) && arr.indexOf(name) === index);
}

function normalizeSearchText(value) {
  return String(value)
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/đ/g, "d");
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function productVisualType(product) {
  const text = `${product.en} ${product.vi} ${product.zh}`.toLowerCase();
  if (/lobster|crayfish|hùm|小龙虾|花龙/.test(text)) return "lobster";
  if (/crab|cua|ghẹ|蟹/.test(text)) return "crab";
  if (/shrimp|prawn|tôm|虾/.test(text)) return "shrimp";
  if (/squid|octopus|cuttlefish|mực|tuộc|鱿|章鱼|墨鱼|乌贼/.test(text)) return "squid";
  if (/sea grape|rong nho|海葡萄/.test(text)) return "seaweed";
  if (/jellyfish|sứa|海蜇/.test(text)) return "jellyfish";
  if (/sea cucumber|hải sâm|海参/.test(text)) return "cucumber";
  if (/frog|ếch|蛙|田鸡/.test(text)) return "frog";
  if (/snail|ốc|螺/.test(text)) return "snail";
  if (/abalone|bào ngư|鲍鱼/.test(text)) return "abalone";
  if (/clam|cockle|mussel|oyster|scallop|sò|nghêu|hàu|vẹm|贝|蛤|蚌|蚝|扇贝/.test(text)) return "shell";
  return "fish";
}

