catalogGrid.addEventListener("click", (event) => {
  const toggle = event.target.closest("button[data-panel-toggle]");
  if (!toggle) return;
  const category = toggle.dataset.panelToggle;
  if (expandedCategories.has(category)) {
    expandedCategories.delete(category);
  } else {
    expandedCategories.add(category);
  }
  renderCatalog();
});

function updateProductCardSelection(id) {
  const card = catalogGrid.querySelector(`.product-card[data-product="${id}"]`);
  if (!card) return;
  const isSelected = selectedItems.has(id);
  const button = card.querySelector(".select-item");
  card.classList.toggle("is-selected", isSelected);
  card.setAttribute("aria-pressed", String(isSelected));
  if (button) {
    button.classList.toggle("is-selected", isSelected);
    button.textContent = isSelected ? t("selected") : t("select");
  }
}

function toggleProduct(id) {
  if (selectedItems.has(id)) {
    selectedItems.delete(id);
  } else {
    selectedItems.add(id);
  }
  updateProductCardSelection(id);
  updateSelectedSummary();
}

catalogGrid.addEventListener("click", (event) => {
  const card = event.target.closest(".product-card[data-product]");
  if (!card) return;
  toggleProduct(Number(card.dataset.product));
});

catalogGrid.addEventListener("keydown", (event) => {
  if (event.key !== "Enter" && event.key !== " ") return;
  const card = event.target.closest(".product-card[data-product]");
  if (!card) return;
  event.preventDefault();
  toggleProduct(Number(card.dataset.product));
});

