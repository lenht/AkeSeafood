function prepareQuoteRequest(formElement) {
  const form = new FormData(formElement);
  const contactLines = [
    form.get("zalo") && `Zalo: ${form.get("zalo")}`,
    form.get("wechat") && `WeChat: ${form.get("wechat")}`,
    form.get("email") && `Email: ${form.get("email")}`,
  ].filter(Boolean);

  if (!contactLines.length) {
    formStatus.textContent = t("contactNeeded");
    return null;
  }

  const customerName = String(form.get("name") || "").trim();
  const customerEmail = String(form.get("email") || "").trim();
  const requestDetails = String(form.get("message") || "").trim();
  const selected = Array.from(selectedItems).map((id) => {
    const product = products[id];
    return `${product.zh} / ${product.vi} / ${product.en}`;
  });
  const selectedList = selected.length ? selected.map((item, index) => `${index + 1}. ${item}`).join("\n") : "No items selected.";
  const preparedMessage = [
    "New seafood quote request",
    "",
    "CUSTOMER",
    `Name: ${customerName || "Not provided"}`,
    `Website language: ${currentLang.toUpperCase()}`,
    "",
    "CONTACT",
    ...contactLines,
    "",
    "SELECTED SEAFOOD",
    selectedList,
    "",
    "REQUEST DETAILS",
    requestDetails || "No extra notes.",
  ].join("\n");
  navigator.clipboard?.writeText(preparedMessage).catch(() => {});

  const formspreeEndpoint = formElement.action;
  formElement.elements.namedItem("_subject").value = `New seafood quote request${customerName ? ` from ${customerName}` : ""}`;
  formElement.elements.namedItem("_replyto").value = customerEmail;
  formElement.elements.namedItem("Selected seafood").value = selectedList;
  formElement.elements.namedItem("Full request").value = preparedMessage;

  return {
    customerName,
    customerEmail,
    form,
    formspreeEndpoint,
    preparedMessage,
    requestDetails,
    selectedList,
  };
}

quoteForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  const formElement = event.currentTarget;
  const prepared = prepareQuoteRequest(formElement);
  if (!prepared) return;

  if (!prepared.formspreeEndpoint) {
    formStatus.textContent = `${t("formspreeSetup")}\n\n${t("requestReady")}\n\n${prepared.preparedMessage}`;
    return;
  }

  const requestPayload = new FormData();
  requestPayload.append("Customer name", prepared.customerName || "Not provided");
  requestPayload.append("Zalo", prepared.form.get("zalo") || "Not provided");
  requestPayload.append("WeChat", prepared.form.get("wechat") || "Not provided");
  requestPayload.append("Customer email", prepared.customerEmail || "Not provided");
  requestPayload.append("Website language", currentLang.toUpperCase());
  requestPayload.append("Selected seafood", prepared.selectedList);
  requestPayload.append("Request details", prepared.requestDetails || "No extra notes.");
  requestPayload.append("Full request", prepared.preparedMessage);
  requestPayload.append("_subject", `New seafood quote request${prepared.customerName ? ` from ${prepared.customerName}` : ""}`);
  if (prepared.customerEmail) requestPayload.append("_replyto", prepared.customerEmail);

  try {
    sendRequestButton.disabled = true;
    formStatus.textContent = t("requestSending");
    const response = await fetch(prepared.formspreeEndpoint, {
      method: "POST",
      body: requestPayload,
      headers: {
        Accept: "application/json",
      },
    });

    if (!response.ok) throw new Error("Formspree request failed");

    formStatus.textContent = t("requestSent");
    formElement.reset();
    selectedItems.clear();
    renderCatalog();
    updateSelectedSummary();
  } catch (error) {
    formStatus.textContent = `${t("requestError")}\n${prepared.preparedMessage}`;
  } finally {
    sendRequestButton.disabled = false;
  }
});

